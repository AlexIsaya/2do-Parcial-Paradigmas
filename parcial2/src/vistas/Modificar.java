    public void seleccionarFila(){ 
        int fila = tabla.getSelectedRow();
        txtDNI.setText(tabla.getModel().getValueAt(fila,0).toString());
        txtCliente1.setText(tabla.getModel().getValueAt(fila,1).toString());
        txtFecha.setText(tabla.getModel().getValueAt(fila,2).toString());
        txtDias.setText(tabla.getModel().getValueAt(fila,3).toString());
        txtDevolver.setText(tabla.getModel().getValueAt(fila,4).toString());
    }
    
    public void cargarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("DNI");modelo.addColumn("Cliente");
        modelo.addColumn("Fecha");modelo.addColumn("CantDIas");modelo.addColumn("Devolver");
        modelo.addColumn("Estado");
        tabla.setModel(modelo);
        String sql = "select * from tabla";
        String datos [] = new String[6];
        try {
            PreparedStatement ps = cn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4); 
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                modelo.addRow(datos);
            }
            tabla.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(Listado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void buscar(){
        String sql = "select * from tabla where DNI='"+txtDNI.getText()+"'";
        try {
            PreparedStatement ps = cn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            if(rs.next()){
                txtDNI.setText(rs.getString("DNI"));
                txtCliente1.setText(rs.getString("Nombre"));
                txtFecha.setText(rs.getString("Fecha"));
                txtDias.setText(rs.getString("CantDias"));
                txtDevolver.setText(rs.getString("Devolver"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Modificar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void modificar(){ 
        //Modificacion de estado
        String item = (String) comboxEstado.getSelectedItem();
        String sql = "update tabla set estado='"+ item
                +"'where DNI='"+txtDNI.getText()+"'";        
        try {
            PreparedStatement ps = cn.prepareStatement(sql);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Estado Actualizado");
            cargarTabla();
        } catch (SQLException ex) {
            Logger.getLogger(Modificar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

