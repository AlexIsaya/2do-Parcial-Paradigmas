    public void cargarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("DNI");modelo.addColumn("Cliente");
        modelo.addColumn("Fecha");modelo.addColumn("CantDIas");modelo.addColumn("Devolver");
        modelo.addColumn("Estado");
        tabla.setModel(modelo);
        String sql = "select * from tabla where Estado = 'No Pagado'";
        String datos [] = new String[6];
        try {
            PreparedStatement ps = cn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4); 
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                modelo.addRow(datos);
            }
            tabla.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(Listado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void calculo(){
        int a = tabla.getRowCount(); //cantidad de filas
        Double tot = 0.0;
        for(int i=0; i<a;i++){
                String total = tabla.getModel().getValueAt(i,4).toString();
            Double tott = Double.parseDouble(total);
            tot = tot + tott;
        }
        System.out.println("Total = " + a);
        String t = Double.toString(tot);
        txtTotal.setText(t);
    }

